    private void updatestockBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatestockBTNActionPerformed
        // TODO add your handling code here:

        int row = stockTable.getSelectedRow();
        if (row >= 0) {
            try {
                Stock s = stm.getStock(row);
                //s.setProductID((Product) productComboBox.getSelectedItem());
                s.setStockName(stockNameTtx.getText());
                s.setQuantity(Integer.parseInt(productQuantityTXT.getText()));

                sr.edit(s);
            } catch (PharmacyException pe) {
                JOptionPane.showMessageDialog(this, "MSG: " + pe.getMessage());
            }

            clear();
            loadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Please , choose what you want to update !");
        }
    }//GEN-LAST:event_updatestockBTNActionPerformed

    private void deleteStockBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStockBTNActionPerformed
        // TODO add your handling code here:
        int row = stockTable.getSelectedRow();
        if (row != -1) {
            Object[] ob = {"Yes", "No"};
            int i = JOptionPane.showOptionDialog(this, "Are u sure you want to delete ?", "Deletion", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                try {
                    Stock s = stm.getStock(row);
                    sr.delete(s);
                    clear();
                    loadTable();
                } catch (PharmacyException ex) {
                    JOptionPane.showMessageDialog(this, "MSG: " + ex.getMessage());
                }
            } else {
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please , choose what you want to delete !");
        }
    }//GEN-LAST:event_deleteStockBTNActionPerformed

